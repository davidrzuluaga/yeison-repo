function changePtoBlue() {
    let allP = document.querySelectorAll("p");
    for (let i = 0; i < allP.length; i++) {
        allP[i].style.color = "blue"
    }
}

function changePtoBlack() {
    let allP = document.querySelectorAll("p");
    for (let i = 0; i < allP.length; i++) {
        allP[i].style.color = "black"
    }
}